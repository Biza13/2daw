/**
 * The function named "resta" takes two parameters, a and b, and returns the result of subtracting b
 * from a.
 * @param a - The function `resta(a, b)` takes two parameters `a` and `b`, and it calculates the
 * difference between `a` and `b` by subtracting `b` from `a`.
 * @param b - The parameter `b` in the `resta` function represents the second number that will be
 * subtracted from the first number `a`.
 * @returns The function `resta` is returning the result of subtracting `b` from `a`.
 */
function resta(a,b){
    return a-b;
}